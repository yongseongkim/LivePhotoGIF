import UIKit

extension UIColor {
    static var gray220: UIColor {
        return UIColor(displayP3Red: 220/255, green: 220/255, blue: 220/255, alpha: 1.0)
    }
    static var gray145: UIColor {
        return UIColor(displayP3Red: 145/255, green: 145/255, blue: 145/255, alpha: 1.0)
    }
    static var gray85: UIColor {
        return UIColor(displayP3Red: 85/255, green: 85/255, blue: 85/255, alpha: 1.0)
    }
    
}
